package br.com.hebaja.ceep.model;

public class Circulo {

    private int cor;
    private String nome;

    public int getCor() {
        return cor;
    }

    public String getNome() {
        return nome;
    }

    public Circulo(int cor, String nome) {
        this.cor = cor;
        this.nome = nome;
    }

}
